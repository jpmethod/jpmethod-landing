/* Workbook product page: renders one catalogue entry into #workbookMain.
   The page shell (generated by scripts/generate_workbook_pages.py) sets the
   workbook slug on <html data-workbook="..."> and includes the modal markup. */

/* ---------- theme (same storage key as the homepage) ---------- */
(function () {
  var btn = document.getElementById('themeBtn');
  function paintBtn() {
    var dark = document.documentElement.dataset.theme === 'dark';
    btn.textContent = dark ? 'Light mode' : 'Dark mode';
  }
  var saved = null;
  try { saved = localStorage.getItem('jpm-theme'); } catch (e) {}
  /* light is the default — the workbooks are white paper; saved preference wins */
  document.documentElement.dataset.theme =
    (saved === 'light' || saved === 'dark') ? saved : 'light';
  paintBtn();
  btn.addEventListener('click', function () {
    var next = document.documentElement.dataset.theme === 'dark' ? 'light' : 'dark';
    document.documentElement.dataset.theme = next;
    try { localStorage.setItem('jpm-theme', next); } catch (e) {}
    paintBtn();
  });
})();

document.getElementById('year').textContent = new Date().getFullYear();

function slug(s) { return s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, ''); }

var galleries = [];

