function factsLine(d) {
  var facts = [];
  /* every workbook is the same 10-section / 20-page format; the distinguishing
     fact is its level band (its step within the subject) */
  if (d.level) facts.push('Level ' + d.level);
  facts.push('Free — answer keys included');
  return facts.join(' · ');
}

function downloadBlock(d) {
  var html = '<div class="getrow">';
  html += '<div class="cta-primary">';
  if (d.pdf_path_a4) {
    html += '<a class="btn a5 big" href="/' + d.pdf_path_a4 + '" download>' +
      'Download the A4 booklet <small>' + d.pdf_size_a4_mb + ' MB</small></a>';
    var alts = '';
    if (d.pdf_path_2up) {
      alts += 'No double-sided printer? <a href="/' + d.pdf_path_2up + '" download>' +
        'Print at home on any printer</a> (' + d.pdf_size_2up_mb + ' MB) — two ' +
        'worksheets per A4 sheet, single-sided. ';
    }
    alts += 'Reading on a tablet? <a href="/' + d.pdf_path + '" download>' +
      'Download the single-page PDF</a> (' + d.pdf_size_mb + ' MB).';
    html += '<span class="cta-secondary">' + alts + '</span>';
  } else {
    html += '<a class="btn a5 big" href="/' + d.pdf_path + '" download>' +
      'Download the PDF <small>' + d.pdf_size_mb + ' MB</small></a>';
    html += '<span class="cta-secondary">One worksheet per page — read it on a ' +
      'tablet or print it on any printer.</span>';
  }
  html += '</div>';
  if (d.pdf_path_a4) {
    html += '<aside class="howtoprint"><h2>How to print the booklet</h2>' +
      '<div class="printfig">' + PRINT_SVG +
      '<ol>' +
        '<li>Print on ordinary A4 paper.</li>' +
        '<li>Choose two-sided (duplex) printing.</li>' +
        '<li>Set <b>flip on short edge</b> — the one setting people miss.</li>' +
        '<li>Fold the stack down the middle and it reads like a book.</li>' +
      '</ol></div></aside>';
  }
  html += '</div>';
  return html;
}

function chaptersBlock(d) {
  if (!d.chapters || !d.chapters.length) return '';
  var html = '<section class="prodsec chapters"><h2>Download a single chapter</h2>' +
    '<p class="intro">Each chapter is a small A4 booklet with its answer key at ' +
    'the back — print it the same way as the full workbook.</p>' +
    '<div class="chapterlist">';
  d.chapters.forEach(function (c) {
    var num = c.number < 10 ? '0' + c.number : '' + c.number;
    html += '<div class="row">' +
      '<span class="num">' + num + '</span>' +
      '<span class="cname">' + c.name + '</span>' +
      '<span class="cpages">' + c.pages + ' pages</span>' +
      '<a class="btn a4" href="/' + c.pdf_path_a4 + '" download>A4 booklet ' +
        '<small>' + c.pdf_size_a4_mb + ' MB</small></a>' +
      '</div>';
  });
  html += '</div></section>';
  return html;
}

function galleryBlock(d) {
  if (!d.samples || !d.samples.length) return '';
  galleries.push({ imgs: d.samples.map(function (s) { return '/' + s; }), name: d.name });
  var html = '<section class="prodsec gallery"><h2>See inside</h2><div class="strip">';
  d.samples.forEach(function (s, i) {
    html += '<button class="thumb" type="button" data-idx="' + i + '" ' +
      'aria-label="Preview sample page ' + (i + 1) + ' of ' + d.name + '">' +
      '<img src="/' + s + '" loading="lazy" alt="Sample page ' + (i + 1) +
      ' of ' + d.name + '"></button>';
  });
  html += '</div></section>';
  return html;
}

function renderWorkbook(d) {
  var badge = d.badge ? '<span class="badge">' + d.badge + '</span>' : '';
  var main = document.getElementById('workbookMain');
  main.innerHTML =
    '<a class="crumbs" href="/">&larr; All workbooks</a>' +
    '<header class="prodhead">' +
      '<h1>' + d.name + badge + '</h1>' +
      '<p class="desc">' + d.description + '</p>' +
      '<p class="facts">' + factsLine(d) + '</p>' +
    '</header>' +
    downloadBlock(d) +
    chaptersBlock(d) +
    galleryBlock(d);

  Array.prototype.forEach.call(main.querySelectorAll('.thumb'), function (b) {
    b.addEventListener('click', function () {
      openModal(0, parseInt(b.getAttribute('data-idx'), 10));
    });
  });
}

function renderNotFound() {
  document.getElementById('workbookMain').innerHTML =
    '<div class="notfound"><h1>Workbook not found</h1>' +
    '<p>This workbook may have moved or been renamed.</p>' +
    '<p><a href="/">Browse all workbooks</a></p></div>';
}

var wanted = document.documentElement.dataset.workbook || '';

fetch('/assets/catalog.json')
  .then(function (r) { return r.json(); })
  .then(function (data) {
    document.getElementById('lastUpdated').textContent =
      new Date(data.generated).toLocaleDateString();
    var all = [];
    (data.sections || []).concat(data.special_sections || []).forEach(function (s) {
      all = all.concat(s.domains || []);
    });
    all = all.concat(data.domains || []);
    var match = null;
    all.forEach(function (d) {
      if (!match && (d.slug || slug(d.name)) === wanted) match = d;
    });
    if (match) renderWorkbook(match); else renderNotFound();
  })
  .catch(function (err) {
    console.error('Error loading catalog:', err);
    document.getElementById('workbookMain').innerHTML =
      '<p class="load-error">The catalogue failed to load. Please refresh the page.</p>';
  });

/* ---------- modal (same behaviour as the homepage) ---------- */
var modal = document.getElementById('imageModal'),
    mImg = document.getElementById('modalImage'),
    mCount = document.getElementById('mCount'),
    mPrev = document.getElementById('mPrev'),
    mNext = document.getElementById('mNext'),
    mClose = document.getElementById('mClose'),
    cur = { g: 0, i: 0 },
    lastFocus = null;

function openModal(g, i) {
  cur = { g: g, i: i };
  paintModal();
  modal.classList.add('open');
  lastFocus = document.activeElement;
  mClose.focus();
}
function paintModal() {
  var gal = galleries[cur.g];
  mImg.src = gal.imgs[cur.i];
  mImg.alt = 'Sample page ' + (cur.i + 1) + ' of ' + gal.name;
  mCount.textContent = gal.name + ' — page ' + (cur.i + 1) + ' of ' + gal.imgs.length;
  var many = gal.imgs.length > 1;
  mPrev.style.display = many ? '' : 'none';
  mNext.style.display = many ? '' : 'none';
}
function step(d) {
  var gal = galleries[cur.g];
  cur.i = (cur.i + d + gal.imgs.length) % gal.imgs.length;
  paintModal();
}
function closeModal() {
  modal.classList.remove('open');
  if (lastFocus && lastFocus.focus) lastFocus.focus();
}

mPrev.addEventListener('click', function (e) { e.stopPropagation(); step(-1); });
mNext.addEventListener('click', function (e) { e.stopPropagation(); step(1); });
mClose.addEventListener('click', closeModal);
modal.addEventListener('click', function (e) { if (e.target === modal) closeModal(); });
document.addEventListener('keydown', function (e) {
  if (!modal.classList.contains('open')) return;
  if (e.key === 'Escape') closeModal();
  if (e.key === 'ArrowLeft') step(-1);
  if (e.key === 'ArrowRight') step(1);
  if (e.key === 'Tab') {
    /* keep focus inside the dialog */
    var f = [mClose, mPrev, mNext].filter(function (b) { return b.style.display !== 'none'; });
    var first = f[0], last = f[f.length - 1];
    if (e.shiftKey && document.activeElement === first) { last.focus(); e.preventDefault(); }
    else if (!e.shiftKey && document.activeElement === last) { first.focus(); e.preventDefault(); }
    else if (f.indexOf(document.activeElement) === -1) { first.focus(); e.preventDefault(); }
  }
});
